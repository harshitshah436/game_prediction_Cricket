package com.cricket;

public class CricketAttribute {

	private String result;
	private String toss_won;
	private String day_night;
	private String home_advantage;
	private String first_bat;
	private String weather;
	private Integer run_target;
	private Integer top_score;
	private Integer top_partnership;

	public String getResult() {
		return result;
	}

	public void setResult(String result) {
		this.result = result;
	}

	public String getToss_won() {
		return toss_won;
	}

	public void setToss_won(String toss_won) {
		this.toss_won = toss_won;
	}

	public String getDay_night() {
		return day_night;
	}

	public void setDay_night(String day_night) {
		this.day_night = day_night;
	}

	public String getHome_advantage() {
		return home_advantage;
	}

	public void setHome_advantage(String home_advantage) {
		this.home_advantage = home_advantage;
	}

	public String getFirst_bat() {
		return first_bat;
	}

	public void setFirst_bat(String first_bat) {
		this.first_bat = first_bat;
	}

	public String getWeather() {
		return weather;
	}

	public void setWeather(String weather) {
		this.weather = weather;
	}

	public Integer getRun_target() {
		return run_target;
	}

	public void setRun_target(Integer run_target) {
		this.run_target = run_target;
	}

	public Integer getTop_score() {
		return top_score;
	}

	public void setTop_score(Integer top_score) {
		this.top_score = top_score;
	}

	public Integer getTop_partnership() {
		return top_partnership;
	}

	public void setTop_partnership(Integer top_partnership) {
		this.top_partnership = top_partnership;
	}

}